export const Icon = ({
  className,
  type,
  height,
  width,
  viewBox,
  onClick,
}) => {
  const IconComponent = icons[type];

  if (!IconComponent) {
    return null;
  }

  const sizeProps =
    width && height ? { width, height, viewBox: viewBox ? `0 0 ${viewBox?.width} ${viewBox?.height}` : undefined } : {};


  return <img src={IconComponent} onClick={onClick} {...sizeProps} className={className}/>
  // return <IconComponent onClick={onClick} {...sizeProps} className={className} />;
};
